var side = 50

var grassArr = [];
var grassEaterArr = [];
var eaterEaterArr = [];
var antArr = [];



function setup() {


    frameRate(4);
    createCanvas(matrix[0].length * side, matrix.length * side);
    background('#acacac');
    var gr = new Grass(2, 2);
    var emptyCells = gr.chooseCell(0);
    console.log(emptyCells);
    for (var y = 0; y < matrix.length; ++y) {
        for (var x = 0; x < matrix[y].length; ++x) {
            if (matrix[y][x] == 1) {
                var gr = new Grass(x, y);
                grassArr.push(gr);
            }
            else if (matrix[y][x] == 2) {
                var fr = new GrassEater(x, y);
                grassEaterArr.push(fr)
            }
            else if (matrix[y][x] == 3) {
                var er = new EaterEater(x, y);
                eaterEaterArr.push(er)

            }
            else if (matrix[y][x] == 4) {
                var ant = new Ant(x, y);
                antArr.push(ant)
            }

        }
    }


}
function draw() {

    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[y].length; x++) {

            if (matrix[y][x] == 1) {
                fill("green");
            }
            else if (matrix[y][x] == 0) {
                fill("#a16c28");
            }

            else if (matrix[y][x] == 2) {
                fill("yellow");
            }

            else if (matrix[y][x] == 3) {
                fill("black");
            }

            else if (matrix[y][x] == 4) {
                fill("pink");
            }


            rect(x * side, y * side, side, side);




        }
    }
    for (var i in grassArr) {
        grassArr[i].mul();

    }
    for (var i in grassEaterArr) {
        grassEaterArr[i].eat();
    }
    for (var i in eaterEaterArr) {
        eaterEaterArr[i].eat();
    }
    for (var i in antArr) {
        antArr[i].move();
    }
}